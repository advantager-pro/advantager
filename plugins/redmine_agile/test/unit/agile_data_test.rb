require File.expand_path('../../test_helper', __FILE__)

class AgileDataTest < ActiveSupport::TestCase
  fixtures :projects,
           :users,
           :roles,
           :members,
           :member_roles,
           :issues,
           :issue_statuses,
           :versions,
           :trackers,
           :projects_trackers,
           :issue_categories,
           :enabled_modules,
           :enumerations,
           :attachments,
           :workflows,
           :custom_fields,
           :custom_values,
           :custom_fields_projects,
           :custom_fields_trackers,
           :time_entries,
           :journals,
           :journal_details,
           :queries

  # Replace this with your real tests.
  def test_save_color
    issue = Issue.find(1)
    issue.agile_data.position = 1
    assert issue.save
    issue.reload
    assert_equal 1, issue.agile_data.position
  end

  def test_delete_color
    issue = Issue.find(1)
    issue.agile_data.position = 1
    assert issue.save
    issue.reload
    agile_data = issue.agile_data
    assert issue.destroy
    assert !AgileData.exists?(agile_data.id)
  end
end
